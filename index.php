function getRandomQuestions($questions, $num = 10) {
    $keys = array_keys($questions);
    shuffle($keys); 
    $selectedKeys = array_slice($keys, 0, $num); 
    return array_intersect_key($questions, array_flip($selectedKeys)); 
}

